package pojo_Pag;

public class Food {
     private int F_id;
     private String S_name;
     private String F_number;
     private float F_price;
     private String F_introduce;
     private String F_images;
     private String F_name;
	
	public String getF_number() {
		return F_number;
	}
	public void setF_number(String f_number) {
		F_number = f_number;
	}
	public int getF_id() {
		return F_id;
	}
	public void setF_id(int f_id) {
		F_id = f_id;
	}
	public float getF_price() {
		return F_price;
	}
	public void setF_price(float f_price) {
		F_price = f_price;
	}
	
	public String getF_images() {
		return F_images;
	}
	public void setF_images(String f_images) {
		F_images = f_images;
	}
	public String getF_name() {
		return F_name;
	}
	public void setF_name(String f_name) {
		F_name = f_name;
	}
	public String getF_introduce() {
		return F_introduce;
	}
	public void setF_introduce(String f_introduce) {
		F_introduce = f_introduce;
	}
	public String getS_name() {
		return S_name;
	}
	public void setS_name(String s_name) {
		S_name = s_name;
	}
     

}